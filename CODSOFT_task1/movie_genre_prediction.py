def load_train(path):
    records = []
    with open(path, encoding="utf-8", errors="replace") as f:
        for line in f:
            parts = line.strip().split(" ::: ")
            if len(parts) >= 4:
                records.append({
                    "id":          parts[0].strip(),
                    "title":       parts[1].strip(),
                    "genre":       parts[2].strip(),
                    "description": parts[3].strip()
                })
    return pd.DataFrame(records)


def load_test(path):
    records = []
    with open(path, encoding="utf-8", errors="replace") as f:
        for line in f:
            parts = line.strip().split(" ::: ")
            if len(parts) >= 3:
                records.append({
                    "id":          parts[0].strip(),
                    "title":       parts[1].strip(),
                    "description": parts[2].strip()
                })
    return pd.DataFrame(records)


def load_solution(path):
    records = []
    with open(path, encoding="utf-8", errors="replace") as f:
        for line in f:
            parts = line.strip().split(" ::: ")
            if len(parts) >= 3:
                records.append({
                    "id":    parts[0].strip(),
                    "genre": parts[2].strip()
                })
    return pd.DataFrame(records)


